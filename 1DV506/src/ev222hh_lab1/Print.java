package ev222hh_lab1;

public class Print {
	public static void main(String[] args) {
		//Print Line på en rad.
		System.out.println("Kunskap är makt!\n");
		
		// "\n" är för ny rad.
		System.out.println("Kunskap\när\nmakt\n");
		
		System.out.println("====================");
		System.out.println("|                  |");
		System.out.println("| Kunskap är makt! |");
		System.out.println("|                  |");
		System.out.println("====================");
	}
}
